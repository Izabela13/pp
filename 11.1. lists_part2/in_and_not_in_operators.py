# OPERATORY in ORAZ not in

numbers = [10, 8, 6, 4, 2]

# Sprawdzenie, czy 5-ka jest elementem w liście "numbers"
print(5 in numbers) # False
# Wynik = False, bo liczba 5 nie jest elementem tego zbioru

# Upewnienie się, że 7-ki nie ma w zbiorze "numbers"
print(7 not in numbers) # True
# Wynik = True, bo liczba 7 faktycznie nie występuje w zbiorze numbers

# Wynik operatorów "in" i "not in" to "True" lub "False".